    private void jbtn_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_registerActionPerformed
        String username = jtxf_user.getText().trim();
        String password = new String(jpswd_password.getPassword()).trim();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        UserDAO userDAO = new UserDAO();
        if (userDAO.usernameExists(username)) {
            JOptionPane.showMessageDialog(this, "O nome de utilizador jÃ¡ existe.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        User newUser = new User("Nome do Utilizador", username, password, "email@example.com", "123456789");
        if (userDAO.registarUtilizador(newUser)) {
            JOptionPane.showMessageDialog(this, "Utilizador registado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            new ClientScreen().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao registar utilizador.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtn_registerActionPerformed

    private void jbtn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_loginActionPerformed
        // Pega o username e password inseridos pelo utilizador
        String username = jtxf_user.getText().trim();
        String password = new String(jpswd_password.getPassword()).trim();

        // Verifica se o login Ã© vÃ¡lido e tambÃ©m devolve o tipo de utilizador
        String tipo = autenticarUsuario(username, password);

        if (tipo == null) {
            // Caso o login falhe, mostra uma mensagem de erro
            JOptionPane.showMessageDialog(this, "Username ou Password incorretos", "Erro de Login", JOptionPane.ERROR_MESSAGE);
        } else {
            // Se a autenticaÃ§Ã£o for bem-sucedida, abre o menu correspondente ao tipo de utilizador
            if (tipo.equals("cliente")) {
                // Menu de Cliente
                JOptionPane.showMessageDialog(this, "Bem-vindo, Cliente!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();  // Fecha a tela de login
                new ClientScreen().setVisible(true);  // Abre o menu do cliente
            } else if (tipo.equals("barbeiro")) {
                // Menu de Barbeiro (admin)
                JOptionPane.showMessageDialog(this, "Bem-vindo, Barbeiro/Admin!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();  // Fecha a tela de login
                new BarberScreen().setVisible(true);  // Abre o menu do barbeiro
            }
        }
    }

    private String autenticarUsuario(String username, String password) {
        UserDAO userDAO = new UserDAO();
        return userDAO.autenticar(username, password); // Agora devolve o tipo ("cliente" ou "barbeiro")
    }//GEN-LAST:event_jbtn_loginActionPerformed

