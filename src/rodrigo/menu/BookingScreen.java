    private void preencherComboBoxes() {
// Preenche o ComboBox com os barbeiros
        try {
            Connection con = DriverManager.getConnection("jdbc:sqlite:barber.db");
            Statement stmt = con.createStatement();
// Altera a query para fazer JOIN entre Barbeiros e Utilizadores
            ResultSet rs = stmt.executeQuery(
                    "SELECT u.nome "
                    + "FROM Barbeiros b "
                    + "JOIN Utilizadores u ON b.utilizador_id = u.id"
            );

            while (rs.next()) {
                jcb_barbeiro.addItem(rs.getString("nome"));
            }
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }

// Preenche o ComboBox com os serviços
        try {
            Connection con = DriverManager.getConnection("jdbc:sqlite:barber.db");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT servico_nome FROM servicos");
            while (rs.next()) {
                jcb_servico.addItem(rs.getString("servico_nome"));
            }
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
