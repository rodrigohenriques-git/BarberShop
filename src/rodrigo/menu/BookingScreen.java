    private void jbtn_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_confirmActionPerformed
        String data = jtxt_data.getText().trim();
        String hora = jtxt_hora.getText().trim();
        String barbeiro = (String) jcb_barbeiro.getSelectedItem();
        String servico = (String) jcb_servico.getSelectedItem();

        if (data.isEmpty() || hora.isEmpty() || barbeiro == null || servico == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Preenche todos os campos!", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            Connection con = DriverManager.getConnection("jdbc:sqlite:barber.db");

            // ID do cliente autenticado (adapta isto à tua lógica de login)
            int clienteId = getClienteId(); // <-- Este método tem de devolver o ID do cliente atual

            // Verifica se já existe agendamento para o mesmo horário e barbeiro
            String checkSql = "SELECT * FROM Agendamentos WHERE data = ? AND hora = ? AND barbeiro = ?";
            PreparedStatement checkStmt = con.prepareStatement(checkSql);
            checkStmt.setString(1, data);
            checkStmt.setString(2, hora);
            checkStmt.setString(3, barbeiro);
            ResultSet rs = checkStmt.executeQuery();

            if (rs.next()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Esse horário já está ocupado!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            } else {
                // Insere o novo agendamento com cliente_id
                String insertSql = "INSERT INTO Agendamentos (data, hora, barbeiro, servico, cliente_id) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement insertStmt = con.prepareStatement(insertSql);
                insertStmt.setString(1, data);
                insertStmt.setString(2, hora);
                insertStmt.setString(3, barbeiro);
                insertStmt.setString(4, servico);
                insertStmt.setInt(5, clienteId);
                insertStmt.executeUpdate();

                javax.swing.JOptionPane.showMessageDialog(this, "Agendamento confirmado!");
            }

            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao agendar. Tenta novamente.", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtn_confirmActionPerformed

